/**
 * Custom Style Dictionary formats for Swift / SwiftUI output.
 */

function hexOnly(hex) {
  return hex.replace('#', '').toUpperCase();
}

function camelCase(path) {
  return path
    .map((p, i) => (i === 0 ? p : p.charAt(0).toUpperCase() + p.slice(1)))
    .join('');
}

function pascalCase(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

function fontWeightToSwift(weight) {
  const map = {
    325: '.regular',
    400: '.regular',
    500: '.medium',
    600: '.semibold',
    700: '.bold',
  };
  return map[weight] || `.init(rawValue: ${weight})`;
}

/**
 * swift/wldColorDefaults – Generates extension WLDColor with static let constants.
 * Matches the existing `WLDColor+Defaults.swift` pattern.
 */
export const swiftColorDefaults = {
  name: 'swift/wldColorDefaults',
  format: ({ dictionary }) => {
    const tokens = dictionary.allTokens.filter((t) => t.$type === 'color');

    const lines = tokens.map((token) => {
      const name = camelCase(token.path);
      const hex = hexOnly(token.$value || token.value);
      return `    static let ${name} = WLDColor("${hex}")`;
    });

    return [
      'import Foundation',
      '',
      '// Generated by World Design System – do not edit manually',
      'extension WLDColor {',
      ...lines,
      '}',
      '',
    ].join('\n');
  },
};

/**
 * swift/wldColorTheme – Generates a struct (LightTheme / DarkTheme) with
 * hex string constants for semantic colors.
 */
export const swiftColorTheme = {
  name: 'swift/wldColorTheme',
  format: ({ dictionary, options }) => {
    const structName = options.structName || 'LightTheme';
    const tokens = dictionary.allTokens.filter(
      (t) => t.$type === 'color' && t.path[0] === 'semantic',
    );

    const lines = tokens.map((token) => {
      const name = camelCase(token.path.slice(1));
      const hex = hexOnly(token.$value || token.value);
      return `    static let ${name} = WLDColor("${hex}")`;
    });

    return [
      'import Foundation',
      '',
      `// Generated by World Design System – do not edit manually`,
      `enum ${structName} {`,
      ...lines,
      '}',
      '',
    ].join('\n');
  },
};


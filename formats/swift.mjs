/**
 * swift/components – Generates component tokens (border radius, buttons, shadows).
 */
export const swiftComponents = {
  name: 'swift/components',
  format: ({ dictionary }) => {
    const tokens = dictionary.allTokens.filter(
      (t) => t.path[0] === 'component',
    );

    const sections = [];

    // Border radius
    const radiusTokens = tokens.filter((t) => t.path[1] === 'borderRadius');
    if (radiusTokens.length) {
      sections.push('    // MARK: - Border Radius');
      radiusTokens.forEach((t) => {
        const name = t.path[t.path.length - 1];
        const v = t.$value ?? t.value;
        sections.push(
          `    static let borderRadius${pascalCase(name)}: CGFloat = ${v}`,
        );
      });
      sections.push('');
    }

    // Buttons
    const buttonTokens = tokens.filter((t) => t.path[1] === 'button');
    if (buttonTokens.length) {
      sections.push('    // MARK: - Button Sizes');
      buttonTokens.forEach((t) => {
        const size = t.path[2];
        const prop = t.path[3];
        const v = t.$value ?? t.value;
        sections.push(
          `    static let button${pascalCase(size)}${pascalCase(prop)}: CGFloat = ${v}`,
        );
      });
      sections.push('');
    }

    // Shadows
    const shadowTokens = tokens.filter((t) => t.path[1] === 'shadow');
    if (shadowTokens.length) {
      sections.push('    // MARK: - Shadows');
      shadowTokens.forEach((t) => {
        const name = t.path[2];
        const v = t.$value ?? t.value;
        sections.push(
          `    static let shadow${pascalCase(name)} = WDSShadow(`,
          `        color: "${hexOnly(v.color)}",`,
          `        alpha: ${v.alpha},`,
          `        offsetX: ${v.offsetX},`,
          `        offsetY: ${v.offsetY},`,
          `        blur: ${v.blur},`,
          `        cornerRadius: ${v.cornerRadius}`,
          '    )',
        );
      });
    }

    return [
      'import CoreGraphics',
      '',
      '// Generated by World Design System – do not edit manually',
      'public struct WDSShadow {',
      '    public let color: String',
      '    public let alpha: Double',
      '    public let offsetX: CGFloat',
      '    public let offsetY: CGFloat',
      '    public let blur: CGFloat',
      '    public let cornerRadius: CGFloat',
      '}',
      '',
      'public enum WDSComponents {',
      ...sections,
      '}',
      '',
    ].join('\n');
  },
};
